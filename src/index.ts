/**
 * Add takes two numbers and returns a number.
 * @param {number} n1 - number - The first number to add
 * @param {number} n2 - number - This is the second parameter, and it's a number.
 */
export const add = (n1: number, n2: number): number => n1 + n2;
/**
 * Multiply takes two numbers and returns a number.
 * @param {number} n1 - number - The first number to multiply
 * @param {number} n2 - number - The second number to multiply
 */
export const multiply = (n1: number, n2: number): number => n1 * n2;
/**
 * Subtract takes two numbers and returns a number.
 * @param {number} n1 - number - The first number to subtract
 * @param {number} n2 - number - This is the second parameter, and it's a number.
 */
export const subtract = (n1: number, n2: number): number => n1 - n2;
/**
 * Divide takes two numbers and returns a number.
 * @param {number} n1 - number - The first number to divide
 * @param {number} n2 - number - This is the second parameter, and it's a number.
 */
export const divide = (n1: number, n2: number): number => n1 / n2;
